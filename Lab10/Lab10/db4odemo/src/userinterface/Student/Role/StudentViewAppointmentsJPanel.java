    public void populateTable()
    {
        
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        
        model.setRowCount(0);
        for (AppointmentWorkRequest r : studentOrganization.getAppointmentWorkQueue().getAppointmentWorkRequestList()){
            String result = ((AppointmentBookingWorkRequest)r).getBookingResult();
            if(!"Checked In".equals(result))
            {
            Object[] row = new Object[5];
            row[0] = r;
            row[1]= r.getSender().getEmployee().getName();
            row[2] = r.getDate();
            row[3] = r.getTime();
            row[4] = r.getStatus();
            if(result==null)
            {
               row[4] = r.getStatus();
            }
            else
            {
                row[4] = result;
            }
            
            model.addRow(row);
            }
        }
    }

