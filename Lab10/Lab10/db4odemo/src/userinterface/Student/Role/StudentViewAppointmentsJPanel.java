    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed
        try{
       
       int selectedRow = workRequestJTable.getSelectedRow();
       
       AppointmentBookingWorkRequest request = (AppointmentBookingWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
 
       if(request.getBookingResult()==null)
       {
       request.setReceiver(userAccount);
       JOptionPane.showMessageDialog(null,"Appointment Booked Successfully");
       request.setBookingResult("Booked");
       populateTable();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Appointment is already booked");
        }
    }
        catch(ArrayIndexOutOfBoundsException e)
        {
            JOptionPane.showMessageDialog(null, "Please select any record");
        }
        
    }//GEN-LAST:event_requestTestJButtonActionPerformed

